
public class KlasaZbir {

	    private int broj1;
	    private int broj2;
	    
	    // Конструктор за класата Calculator
	    public KlasaZbir(int br1, int br2) {
	        this.broj1 = br1;
	        this.broj2 = br2;
	    }
	    
	    // Метод за собирање на броеви
	    public int Soberi() {
	        return broj1 + broj2;
	    }
	}

